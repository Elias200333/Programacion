package tarea2;

public class Ejercicio1 {
	public static void main(String[] args) {
		/*El indice "i" nos sirve como contador en el bucle while, para que en el momento que inicia
		 * el bucle, nos sirva como limitador, mientras que dentro del bucle lo incrementamos.*/
		int i = 0;
		while (i < 10) {
			System.out.println("los bucles son muy sencillos");
			i++;
		}
	}
}
