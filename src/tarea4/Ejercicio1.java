package tarea4;

public class Ejercicio1 {
	public static void main(String[] args) {
		/*El indice "i" nos sirve como contador en el bucle for, para que en el momento que inicia
		 * el bucle, nos sirva como limitador, mientras que dentro del bucle lo incrementamos.*/
		for (int i = 0; i < 10; i++) { // Bucle que indica un indice 0, junto a una constante en aumento.
			System.out.println("los bucles son muy sencillos"); 
		}
	}
}
